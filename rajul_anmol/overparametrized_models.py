# Experiment Loop
# ======================
results = {}

for depth in depths:
    for act in activations:
        print(f"\n=== Depth={depth}, Activation={act} ===")

        model = CNN(depth, act).to(device)
        optimizer = optim.Adam(model.parameters(), lr=0.001)
        criterion = nn.CrossEntropyLoss()

        train_hist, val_hist = [], []

        for ep in range(epochs):
            train_acc = train(model, trainloader, optimizer, criterion)
            val_acc, _ = evaluate(model, valloader)

            train_hist.append(train_acc)
            val_hist.append(val_acc)

            print(f"Epoch {ep+1}: Train={train_acc:.4f}, Val={val_acc:.4f}")

        test_acc, test_margins = evaluate(model, testloader)
        train_acc_final, train_margins = evaluate(model, trainloader)

        gap = train_acc_final - test_acc

        results[(act, depth)] = {
            "train_hist": train_hist,
            "val_hist": val_hist,
            "test_acc": test_acc,
            "gap": gap,
            "train_margins": train_margins,
            "test_margins": test_margins
        }

