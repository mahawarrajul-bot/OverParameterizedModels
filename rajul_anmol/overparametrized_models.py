# ======================
# Activation
# ======================
def get_activation(name):
    return {
        "relu": nn.ReLU(),
        "tanh": nn.Tanh(),
        "sigmoid": nn.Sigmoid(),
        "gelu": nn.GELU()
    }[name]

# ======================
# CNN Model
# ======================
class CNN(nn.Module):
    def __init__(self, depth, activation):
        super().__init__()

        act = get_activation(activation)
        layers = []

        in_channels = 3
        channels = 32

        # Convolutional blocks
        for i in range(depth): # i is 0-indexed block number
            layers.append(nn.Conv2d(in_channels, channels, kernel_size=3, padding=1))

            # Calculate spatial dimension after previous pooling and current conv
            # Assuming 32x32 initial input. Each MaxPool2d halves the dimension.
            # So, after `i` MaxPool2d operations (from previous blocks), the current spatial dim is 32 / (2**i).
            current_spatial_dim = 32 // (2**i)

            # Only add BatchNorm2d if spatial dimensions are not 1x1
            if current_spatial_dim > 1:
                layers.append(nn.BatchNorm2d(channels))
            else:
                # If spatial dimension is 1x1, use nn.Identity() instead of BatchNorm2d
                layers.append(nn.Identity()) # Placeholder, effectively no-op

            layers.append(act)

            # Apply MaxPool2d only if current_spatial_dim is greater than 1
            if current_spatial_dim > 1:
                layers.append(nn.MaxPool2d(2))
            else:
                # If spatial dimension is 1x1, use nn.Identity() instead of MaxPool2d
                layers.append(nn.Identity()) # Placeholder for MaxPool2d

            in_channels = channels
            channels *= 2

        self.conv = nn.Sequential(*layers)

        # Compute flattened size dynamically
        with torch.no_grad():
            dummy = torch.zeros(1, 3, 32, 32)
            out = self.conv(dummy)
            self.flat_dim = out.view(1, -1).shape[1]

        self.fc = nn.Linear(self.flat_dim, 10)

    def forward(self, x):
        x = self.conv(x)
        x = x.view(x.size(0), -1)
        return self.fc(x)

# ======================
# Train Function
# ======================
def train(model, loader, optimizer, criterion):
    model.train()
    correct, total = 0, 0

    for x, y in loader:
        x, y = x.to(device), y.to(device)

        optimizer.zero_grad()
        out = model(x)
        loss = criterion(out, y)
        loss.backward()
        optimizer.step()

        pred = out.argmax(dim=1)
        correct += (pred == y).sum().item()
        total += y.size(0)

    return correct / total

# ======================
# Evaluation + Margins
# ======================
def evaluate(model, loader):
    model.eval()
    correct, total = 0, 0
    margins = []

    with torch.no_grad():
        for x, y in loader:
            x, y = x.to(device), y.to(device)
            out = model(x)

            pred = out.argmax(dim=1)
            correct += (pred == y).sum().item()
            total += y.size(0)

            # Vectorized margin computation
            correct_scores = out[torch.arange(len(y)), y]

            temp = out.clone()
            temp[torch.arange(len(y)), y] = -1e9
            second_best = temp.max(dim=1).values

            batch_margins = (correct_scores - second_best).cpu().numpy()
            margins.extend(batch_margins)

    return correct / total, margins

